= NAME

Kwid - A Kwiki Text Substitute for POD

= SYNOPSIS

You are soaking in it.

= DESCRIPTION

KWID is a feature-to-feature substitute for POD. See
http://pugs.kwiki.org/?KwidProposal for the specification.

The main selling points are:

* Kwid is cleaner
* The markup reads better than POD when unformatted
* Lists are so much easier

