# low level Math module

$::Math = make_class(name=>"Math",parent=>[$meta_Num],methods=>{
    srand => sub {
                  my $self = shift;
                  my $seed = $_[0]{_value};
                  ::DISPATCH($::Num, "new", srand($seed));
                 },
    rand => sub {
                 my $self = shift;
                 my $expr = $_[0]{_value} || 1;
                 ::DISPATCH($::Num, "new", rand($expr));
                },
 });

1;
