package KindaPerl6;

our $VERSION = '0.01';

# This is a Perl 5 .pm file because:
#
# * It houses the global KindaPerl6 version number
#
# * It can be used to locate where in the Perl module tree it is with
#   C<require KindaPerl6; my $path = $INC{"KindaPerl6.pm"}>, we can
#   then store runtime files and other voodoo compiled stuff in
#   KindaPerl6/$something

1;

__END__

