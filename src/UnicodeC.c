int stg_hack_u_towupper (int c)
{
	const struct _convrule_ *rule=stg_hack_getrule(stg_hack_convchars,NUM_CONVBLOCKS,c);
	if(rule==&stg_hack_nullrule) return c;
	return c+rule->updist;
}

int stg_hack_u_towlower (int c)
{
	const struct _convrule_ *rule=stg_hack_getrule(stg_hack_convchars,NUM_CONVBLOCKS,c);
	if(rule==&stg_hack_nullrule) return c;
	return c+rule->lowdist;
}

